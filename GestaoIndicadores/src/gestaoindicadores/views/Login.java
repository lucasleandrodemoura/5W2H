        if(crud.isConectado()){
            crud.select("SELECT * FROM usuarios WHERE login = '"+this.jLogin.getText()+"' AND senha = '"+this.jSenha.getPassword().toString());
        }else {
            JOptionPane.showMessageDialog(null, "Você não esta conectado a nenhuma base de dados");
        }
